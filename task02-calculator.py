num1 = int(input("Enter First Number: "))
num2 = int(input("Enter Second Number: "))
opr=input("Enter the opr..")
if opr=="+":
    print(num1+num2)
elif opr=="-":
    print(num1-num2)
elif opr=="*":
    print(num1*num2)
elif opr=="/":
    print(num1/num2)            
else:
    print("invalid opr....")                    
